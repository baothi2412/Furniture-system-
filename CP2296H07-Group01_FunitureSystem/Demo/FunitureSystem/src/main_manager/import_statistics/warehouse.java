package main_manager.import_statistics;

/**
 *
 * @author Duy Nguyen
 */
public class warehouse {
    private String name;
    private int total;

    public warehouse() {
    }

    public warehouse(String name, int total) {
        this.name = name;
        this.total = total;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
    
}
