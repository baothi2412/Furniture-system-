    private void btnAddAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAcceptActionPerformed
        String id = txtID.getText();
        String name = txtName.getText();
        String address = txtAddress.getText();
        if (id.isEmpty() || name.isEmpty() || address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter full information to add warehouse!!");
        } else {
            boolean temp = true;
            for (char c : id.toCharArray()) {
                if (!Character.isDigit(c)) {
                    JOptionPane.showMessageDialog(this, "Warehouse ID only accepts numbers, cannot contain characters!!","Error", JOptionPane.ERROR_MESSAGE);
                    temp = false;
                    break;
                }
            }
            
            if (temp) {
                boolean checkID = mnWare.checkID(id);
                if (!checkID) {
                    JOptionPane.showMessageDialog(this, "Error! Warehouse ID is duplicate.","Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    mnWare.addWarehouse(id, name, address);
                    JOptionPane.showMessageDialog(this, "Add New Warehouse Successfully!");
                    Add_Warehouse addWare = new Add_Warehouse();
                    this.dispose();
                    addWare.show();
                }
            }
        }
    }//GEN-LAST:event_btnAddAcceptActionPerformed

